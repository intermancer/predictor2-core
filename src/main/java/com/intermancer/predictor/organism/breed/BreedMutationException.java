package com.intermancer.predictor.organism.breed;

@SuppressWarnings("serial")
public class BreedMutationException extends RuntimeException {

	public BreedMutationException() {
		super();
	}

	public BreedMutationException(String message, Throwable cause) {
		super(message, cause);
	}

	public BreedMutationException(String message) {
		super(message);
	}

	public BreedMutationException(Throwable cause) {
		super(cause);
	}

}
